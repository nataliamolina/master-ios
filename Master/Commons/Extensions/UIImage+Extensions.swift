import UIKit

extension UIImage {
    static var greenLogo: UIImage? {
        return UIImage(named: "green_logo")
    }
    
    static var greenLogoIcon: UIImage? {
        return UIImage(named: "green_logo_icon")
    }
    
    static var openEye: UIImage? {
        return UIImage(named: "eye")
    }
    
    static var closedEye: UIImage? {
        return UIImage(named: "eye_closed")
    }
}
